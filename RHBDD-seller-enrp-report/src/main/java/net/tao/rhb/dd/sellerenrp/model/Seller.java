package net.tao.rhb.dd.sellerenrp.model;

import org.springframework.batch.core.configuration.annotation.JobScope;

@JobScope
public class Seller {
	
	
	private String seller_id;
	private String enrp_file_block1;
	private String enrp_file_block2;
	public Seller(){}
	public Seller(String seller_id, String enrp_file_block1,
			String enrp_file_block2) {
		super();
		this.seller_id = seller_id;
		this.enrp_file_block1 = enrp_file_block1;
		this.enrp_file_block2 = enrp_file_block2;
	}
	public String getSeller_id() {
		return seller_id;
	}
	public void setSeller_id(String seller_id) {
		this.seller_id = seller_id;
	}
	public String getEnrp_file_block1() {
		return enrp_file_block1;
	}
	public void setEnrp_file_block1(String enrp_file_block1) {
		this.enrp_file_block1 = enrp_file_block1;
	}
	public String getEnrp_file_block2() {
		return enrp_file_block2;
	}
	public void setEnrp_file_block2(String enrp_file_block2) {
		this.enrp_file_block2 = enrp_file_block2;
	}
	@Override
	public String toString() {
		return "Seller [seller_id=" + seller_id + ", enrp_file_block1="
				+ enrp_file_block1 + ", enrp_file_block2=" + enrp_file_block2
				+ ", getSeller_id()=" + getSeller_id()
				+ ", getEnrp_file_block1()=" + getEnrp_file_block1()
				+ ", getEnrp_file_block2()=" + getEnrp_file_block2()
				+ ", getClass()=" + getClass() + ", hashCode()=" + hashCode()
				+ ", toString()=" + super.toString() + "]";
	}
	
	
}
